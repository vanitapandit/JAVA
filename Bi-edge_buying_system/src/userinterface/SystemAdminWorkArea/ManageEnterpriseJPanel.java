    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        String networkName = (String) networkJComboBox.getSelectedItem();
        Network network = system.getNetworkMap().get(networkName);
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeJComboBox.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }

        String name = nameJTextField.getText();

              
        if(enterpriseDirectorText.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please input enterprise director! ");
            return;
        }
        
        if(!emailPatternCorrect(emailJTextField.getText()))
        {
            JOptionPane.showMessageDialog(null, "Please input XX@XX.XXX ");
            return;
        }
        if(passwordJPasswordField.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please input password! ");
            return;
        }
        
        if(network.getEnterpriseDirectory().createAndAddEnterprise(networkName, name, type, network.getNetworkID()) == null)
        {
            JOptionPane.showMessageDialog(null, "Enterprise has existed!");
            return;
        }
        system.getNetworkMap().put(networkName, network);
        
        String username = network.getEnterpriseDirectory().getEnterpriseNameMap().get(name).getEnterpriseID();
  
        
        Role role = new DirectorRole();
        
        
        
        if(system.getUserAccountDirectory().createUserAccount(emailJTextField.getText(), 
                enterpriseDirectorText.getText(), passwordJPasswordField.getText() , username,
                role) != null)
            JOptionPane.showMessageDialog(null, "Account created successfully");
        


        populateTable();

    }//GEN-LAST:event_submitJButtonActionPerformed

    private boolean emailPatternCorrect(String email)
    {
        Pattern p = Pattern.compile("^[a-zA-Z0-9]+@[a-zA-Z0-9]+.[a-zA-Z0-9]+$");
        Matcher m = p.matcher(email);
        boolean b = m.matches();
        return b;
    }
    
