    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for(Map.Entry<String, Network> network : system.getNetworkMap().entrySet()){
            for (Map.Entry<String, Enterprise> enterprise : network.getValue().
                    getEnterpriseDirectory().getEnterpriseNameMap().entrySet()) {
                Object[] row = new Object[3];
                row[0] = enterprise.getKey();
                row[1] = network.getKey();
                row[2] = enterprise.getValue().getEnterpriseType();

                model.addRow(row);
            }
        }
    }

    private void populateComboBox() {
        networkJComboBox.removeAllItems();
        enterpriseTypeJComboBox.removeAllItems();

        for(Map.Entry<String, Network> network : system.getNetworkMap().entrySet()){
            networkJComboBox.addItem(network.getKey());
        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            enterpriseTypeJComboBox.addItem(type);
        }

    }

